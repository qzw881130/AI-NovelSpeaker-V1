function localizeDocumentText(root = document) {
  const lang = getLanguage();
  if (lang === "zh-CN") {
    document.documentElement.lang = "zh-CN";
    return;
  }
  document.documentElement.lang = lang;
  if (typeof document.title === "string") {
    document.title = translateText(document.title);
  }
  const walker = document.createTreeWalker(root.body || root, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    const parent = node.parentElement;
    if (parent && parent.closest("pre,code,textarea,script,style")) return;
    const raw = String(node.nodeValue || "");
    let nextValue = raw;
    const trimmed = raw.trim();
    if (!trimmed) return;
    const hit = STATIC_TEXT_MAP[trimmed];
    if (hit && hit[lang]) {
      nextValue = nextValue.replace(trimmed, hit[lang]);
    }
    for (const [token, table] of Object.entries(TOKEN_MAP)) {
      const target = table[lang];
      if (!target) continue;
      if (nextValue.includes(token)) {
        nextValue = nextValue.split(token).join(target);
      }
    }
    node.nodeValue = nextValue;
  });

  const scope = root.body || root;
  const attrs = ["placeholder", "title", "aria-label"];
  if (scope && scope.querySelectorAll) {
    scope.querySelectorAll("*").forEach((el) => {
      attrs.forEach((attr) => {
        if (!el.hasAttribute || !el.hasAttribute(attr)) return;
        const raw = el.getAttribute(attr) || "";
        if (!raw) return;
        const translated = translateText(raw);
        if (translated !== raw) {
          el.setAttribute(attr, translated);
        }
      });
    });
  }
}

export { getLanguage, localizeDocumentText, t, translateBackendError, translateText };
