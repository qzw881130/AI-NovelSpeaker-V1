function getLanguage() {
  const lang = String(localStorage.getItem(UI_LANG_KEY) || DEFAULT_LANGUAGE);
  return SUPPORTED_LANGUAGES.has(lang) ? lang : DEFAULT_LANGUAGE;
}

function template(text, vars = {}) {
  return String(text || "").replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
}

function translateText(input) {
  let output = String(input || "");
  const lang = getLanguage();
  if (lang === "zh-CN" || !output) return output;
  const trimmed = output.trim();
  const hit = STATIC_TEXT_MAP[trimmed];
  if (hit && hit[lang]) {
    output = output.replace(trimmed, hit[lang]);
  }
  for (const [token, table] of Object.entries(TOKEN_MAP)) {
    const target = table[lang];
    if (!target) continue;
    if (output.includes(token)) output = output.split(token).join(target);
  }
  return output;
}

function t(key, vars = {}) {
  const lang = getLanguage();
  const table = MESSAGES[lang] || MESSAGES[DEFAULT_LANGUAGE];
  const base = table[key] ?? MESSAGES[DEFAULT_LANGUAGE][key] ?? key;
  return template(base, vars);
}

function translateBackendError(raw) {
  const msg = String(raw || "").trim();
  if (!msg) return msg;
  const lower = msg.toLowerCase();
  for (const [pattern, key] of Object.entries(BACKEND_ERROR_MAP)) {
    if (lower.includes(pattern)) return t(key);
  }
  return msg;
}

